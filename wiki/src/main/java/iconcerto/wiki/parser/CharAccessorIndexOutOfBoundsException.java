package iconcerto.wiki.parser;

public class CharAccessorIndexOutOfBoundsException extends
		CharAccessorRuntimeException {

	private static final long serialVersionUID = 6672602230121741783L;

	public CharAccessorIndexOutOfBoundsException() {
		super();
	}

	public CharAccessorIndexOutOfBoundsException(String message, Throwable cause) {
		super(message, cause);
	}

	public CharAccessorIndexOutOfBoundsException(String message) {
		super(message);
	}

	public CharAccessorIndexOutOfBoundsException(Throwable cause) {
		super(cause);
	}

}
