package iconcerto.wiki.parser;

public class CharAccessorRuntimeException extends ParserRuntimeException {

	private static final long serialVersionUID = -4797895015784521292L;

	public CharAccessorRuntimeException() {
		super();
	}

	public CharAccessorRuntimeException(String message, Throwable cause) {
		super(message, cause);
	}

	public CharAccessorRuntimeException(String message) {
		super(message);
	}

	public CharAccessorRuntimeException(Throwable cause) {
		super(cause);
	}

}
