package iconcerto.wiki.parser;

public class CharAccessorException extends ParserRuntimeException {

	private static final long serialVersionUID = -4797895015784521292L;

	public CharAccessorException() {
		super();
	}

	public CharAccessorException(String message, Throwable cause) {
		super(message, cause);
	}

	public CharAccessorException(String message) {
		super(message);
	}

	public CharAccessorException(Throwable cause) {
		super(cause);
	}

}
